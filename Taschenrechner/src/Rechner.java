public class Rechner {
	
	double addierte(double eingabe1,double eingabe2) {
		return eingabe1+eingabe2;
	}
	
	double subtrahierte(double eingabe1, double eingabe2) {
		return	eingabe1 - eingabe2;
	}
	double multiplizierte(double eingabe1, double eingabe2) {
		return	eingabe1 * eingabe2;
	}
	double dividierte(double eingabe1, double eingabe2) {
		return	eingabe1 / eingabe2;
	}
	double modulo(double eingabe1, double eingabe2) {
		return	eingabe1%eingabe2;
	}
	
	
	
	

}
	